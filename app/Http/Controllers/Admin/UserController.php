    public function login(LoginRequest $request)
    {
        $request->validated();

        $login = Auth::attempt([
            'email' => $request->email,
            'password' => $request->password,
        ]);

        if ($login) {
            return redirect()->route('admin.index')->with('success', 'WELCOME');
        }

        return redirect()->back()->with('error', 'Wrong email and/or password');
    }

